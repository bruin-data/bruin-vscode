#!/usr/bin/env node
// @ts-check
/**
 * Sync the list of Bruin asset types into the VS Code extension.
 *
 * The canonical source of truth for asset types lives in the Bruin CLI repo at
 * `pkg/pipeline/pipeline.go`, declared as `AssetType("...")` literals. This repo
 * keeps its own copies for editor features (YAML validation + completions) which
 * drift out of sync whenever the CLI adds a new asset type (e.g. Tableau).
 *
 * This script fetches that file and propagates the type list to the two places
 * that drive recognition:
 *   1. schemas/yaml-assets-schema.json  -> the `type` enum (YAML validation)
 *   2. src/language-server/providers/assetTypes.ts -> generated completion list
 *
 * Usage:
 *   node scripts/sync-asset-types.mjs                # update files from bruin main
 *   node scripts/sync-asset-types.mjs --ref=v0.11.0  # pin to a tag/branch/sha
 *   node scripts/sync-asset-types.mjs --file=./pipeline.go   # use a local copy
 *   node scripts/sync-asset-types.mjs --check        # CI: fail if out of sync
 */

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const SCHEMA_PATH = join(ROOT, "schemas", "yaml-assets-schema.json");
const ASSET_TYPES_TS_PATH = join(
  ROOT,
  "src",
  "language-server",
  "providers",
  "assetTypes.ts"
);

const SOURCE_FILE = "pkg/pipeline/pipeline.go";

function parseArgs(argv) {
  const args = { check: false, ref: "main", file: null };
  for (const arg of argv) {
    if (arg === "--check") {
      args.check = true;
    } else if (arg.startsWith("--ref=")) {
      args.ref = arg.slice("--ref=".length);
    } else if (arg.startsWith("--file=")) {
      args.file = arg.slice("--file=".length);
    } else {
      console.error(`Unknown argument: ${arg}`);
      process.exit(2);
    }
  }
  return args;
}

async function loadSource({ ref, file }) {
  if (file) {
    return { text: readFileSync(file, "utf8"), origin: file };
  }
  const url = `https://raw.githubusercontent.com/bruin-data/bruin/${ref}/${SOURCE_FILE}`;
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`Failed to fetch ${url}: ${res.status} ${res.statusText}`);
  }
  return { text: await res.text(), origin: url };
}

/** Extract every distinct asset type string, preserving first-seen order. */
function extractAssetTypes(goSource) {
  const re = /AssetType\("([^"]+)"\)/g;
  const seen = new Set();
  const types = [];
  let m;
  while ((m = re.exec(goSource)) !== null) {
    if (!seen.has(m[1])) {
      seen.add(m[1]);
      types.push(m[1]);
    }
  }
  return types;
}

/**
 * Reconcile the canonical list with the order already used in the schema:
 * keep existing types in their current order (minimal diff), drop types the CLI
 * removed, and append newly-added types in the order Bruin declares them.
 */
function reconcile(existing, canonical) {
  const canonicalSet = new Set(canonical);
  const existingSet = new Set(existing);
  const kept = existing.filter((t) => canonicalSet.has(t));
  const added = canonical.filter((t) => !existingSet.has(t));
  const removed = existing.filter((t) => !canonicalSet.has(t));
  return { result: [...kept, ...added], added, removed };
}

// ---- schema (YAML validation) ----------------------------------------------

const TYPE_ENUM_RE =
  /("type":\s*\{\s*"type":\s*"string",\s*"enum":\s*\[)([\s\S]*?)(\]\s*,\s*"description":\s*"The type of the asset")/;

function readSchemaEnum(schemaText) {
  const match = schemaText.match(TYPE_ENUM_RE);
  if (!match) {
    throw new Error(
      "Could not locate the asset `type` enum in yaml-assets-schema.json"
    );
  }
  return [...match[2].matchAll(/"([^"]+)"/g)].map((m) => m[1]);
}

function renderSchema(schemaText, types) {
  const items = types
    .map((t, i) => `            ${JSON.stringify(t)}${i < types.length - 1 ? "," : ""}`)
    .join("\n");
  return schemaText.replace(
    TYPE_ENUM_RE,
    (_full, head, _body, tail) => `${head}\n${items}\n          ${tail}`
  );
}

// ---- completions (generated TS) --------------------------------------------

function renderAssetTypesTs(types) {
  const lines = types.map((t) => `  ${JSON.stringify(t)},`).join("\n");
  return `// AUTO-GENERATED by scripts/sync-asset-types.mjs — do not edit by hand.
// Source of truth: bruin-data/bruin ${SOURCE_FILE}
// Run \`npm run sync:asset-types\` to regenerate.

/** Every asset type recognized by the Bruin CLI. */
export const ASSET_TYPES: string[] = [
${lines}
];
`;
}

function readGeneratedTypes(tsText) {
  return [...tsText.matchAll(/^\s*"([^"]+)",$/gm)].map((m) => m[1]);
}

// ----------------------------------------------------------------------------

function readOptional(path) {
  try {
    return readFileSync(path, "utf8");
  } catch {
    return null;
  }
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const { text: goSource, origin } = await loadSource(args);

  const canonical = extractAssetTypes(goSource);
  if (canonical.length === 0) {
    throw new Error(`No AssetType("...") declarations found in ${origin}`);
  }

  const schemaText = readFileSync(SCHEMA_PATH, "utf8");
  const existing = readSchemaEnum(schemaText);
  const { result, added, removed } = reconcile(existing, canonical);

  const newSchemaText = renderSchema(schemaText, result);
  const newTsText = renderAssetTypesTs(result);
  const currentTsText = readOptional(ASSET_TYPES_TS_PATH);

  const schemaChanged = newSchemaText !== schemaText;
  const tsChanged = newTsText !== currentTsText;

  console.log(`Source: ${origin}`);
  console.log(`Canonical asset types: ${canonical.length}`);
  if (added.length) {
    console.log(`  + added (${added.length}): ${added.join(", ")}`);
  }
  if (removed.length) {
    console.log(`  - removed (${removed.length}): ${removed.join(", ")}`);
  }
  if (!added.length && !removed.length) {
    console.log("  enum already up to date");
  }

  if (args.check) {
    if (schemaChanged || tsChanged) {
      console.error(
        "\nAsset types are out of sync. Run `npm run sync:asset-types` and commit the result."
      );
      process.exit(1);
    }
    console.log("\nAsset types are in sync.");
    return;
  }

  if (schemaChanged) {
    writeFileSync(SCHEMA_PATH, newSchemaText);
    console.log(`Updated ${SCHEMA_PATH}`);
  }
  if (tsChanged) {
    writeFileSync(ASSET_TYPES_TS_PATH, newTsText);
    console.log(`Updated ${ASSET_TYPES_TS_PATH}`);
  }
  if (!schemaChanged && !tsChanged) {
    console.log("Nothing to update.");
  }
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
